SELECT STUDENT.NAME, STUDENT.BDAY FROM STUDENT
	WHERE BDAY IN(SELECT STUDENT.BDAY FROM STUDENT
						GROUP BY STUDENT.BDAY
						HAVING (count(STUDENT.BDAY) > 1))
						ORDER BY STUDENT.BDAY