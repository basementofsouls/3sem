SELECT top 1
	(SELECT avg(PROGRESS.NOTE) FROM PROGRESS
			WHERE PROGRESS.SUBJECT like 'ОАиП') [Средняя по ОАиП],
	(SELECT avg(PROGRESS.NOTE) FROM PROGRESS
			WHERE PROGRESS.SUBJECT like 'КГ') [Средняя по КГ],
	(SELECT avg(PROGRESS.NOTE) FROM PROGRESS
			WHERE PROGRESS.SUBJECT like 'СУБД') [Средняя по СУБД]
	FROM PROGRESS

