SELECT FACULTY.FACULTY_NAME[Наименование факультета], PULPIT.PULPIT[Кафедра] 
		FROM FACULTY, PULPIT
			WHERE not exists (SELECT * FROM PULPIT WHERE FACULTY.FACULTY = PULPIT.FACULTY)

select FACULTY.FACULTY_NAME, PULPIT.PULPIT[Кафедра] from FACULTY, PULPIT
	where exists (select * from PULPIT where FACULTY.FACULTY = PULPIT.FACULTY)

