SELECT FACULTY.FACULTY[Факультет], PULPIT.PULPIT_NAME[Кафедры], PROFESSION.PROFESSION_NAME[Технологические специальности]
		FROM FACULTY, PULPIT, PROFESSION
		WHERE FACULTY.FACULTY = PULPIT.FACULTY AND FACULTY.FACULTY = PROFESSION.FACULTY
		AND
		PROFESSION.PROFESSION_NAME In(SELECT PROFESSION.PROFESSION_NAME FROM PROFESSION 
									WHERE (PROFESSION.PROFESSION_NAME LIKE '%технологи%'))

--№2
SELECT FACULTY.FACULTY[Факультет], PULPIT.PULPIT_NAME[Кафедры], PROFESSION.PROFESSION_NAME[Технологические специальности]
		FROM FACULTY
		Inner Join PULPIT ON FACULTY.FACULTY = PULPIT.FACULTY
		Inner Join PROFESSION ON FACULTY.FACULTY = PROFESSION.FACULTY
		WHERE PROFESSION_NAME In(SELECT PROFESSION.PROFESSION_NAME FROM PROFESSION 
											WHERE (PROFESSION.PROFESSION_NAME LIKE '%технологи%'))

--№3
SELECT FACULTY.FACULTY[Факультет], PULPIT.PULPIT_NAME[Кафедры], PROFESSION.PROFESSION_NAME[Технологические специальности]
		FROM FACULTY
		Inner Join PULPIT ON FACULTY.FACULTY = PULPIT.FACULTY
		Inner Join PROFESSION ON FACULTY.FACULTY = PROFESSION.FACULTY
		WHERE (PROFESSION_NAME LIKE '%технологи%')
